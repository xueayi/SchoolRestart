function writeXlsx(filename, sheetName, headers, labels, rows) {
    const data = [headers, labels, ...rows];
    const ws = XLSX.utils.aoa_to_sheet(data);
    const wb = XLSX.utils.book_new();
    XLSX.utils.book_append_sheet(wb, ws, sheetName);
    const buf = XLSX.write(wb, { type: 'buffer', bookType: 'xlsx' });
    writeFileSync(resolve(dataDir, filename), buf);
    console.log(`  ${filename}: ${rows.length} rows`);
}

const fx = (apr = 0, knw = 0, soc = 0, chm = 0, lov = 0, vit = 0) => ({ apr, knw, soc, chm, lov, vit });

function createAllocator(min, max) {
    let cur = min;
    return () => {
        if (cur > max) throw new Error(`Event ID overflow ${min}-${max}`);
        return cur++;
    };
}

function pickCycle(pool, start, n) {
    const out = [];
    const L = pool.length;
    for (let i = 0; i < n; i++) out.push(pool[(start + i) % L]);
    return out;
}

/** Regular event row (non-choice) */
function rowReg(id, text, grade, post, e, noRand = '', inc = '', exc = '', b1 = '', b2 = '', b3 = '') {
    return [
        id, text, grade, post,
        e.apr, e.knw, e.soc, e.chm, e.lov, e.vit,
        noRand, inc, exc, b1, b2, b3,
        0, '', '', '', '', '', '', '', '', '',
    ];
}

/** Choice event row */
function rowChoice(id, text, grade, post, e, texts, effs, nexts) {
    const t = [...texts, '', '', '', ''].slice(0, 4);
    const ef = [...effs, '', '', '', ''].slice(0, 4);
    const nx = [...nexts, '', '', '', ''].slice(0, 4).map((v) => (v === '' ? '' : String(v)));
    return [
        id, text, grade, post,
        e.apr, e.knw, e.soc, e.chm, e.lov, e.vit,
        1, '', '', '', '', '',
        1, ...t, ...ef, ...nx,
    ];
}

/** Follow-up after choice */
function rowFollow(id, text, grade, post, e) {
    return rowReg(id, text, grade, post, e, 1);
}

