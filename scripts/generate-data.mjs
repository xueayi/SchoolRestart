const STAGE_ROUNDS = [3, 6, 18, 18, 24, 32];
const STAGE_EVT = [8, 10, 20, 25, 25, 30];

function roundToStage(r) {
    let x = r;
    for (let s = 0; s < STAGE_ROUNDS.length; s++) {
        if (x <= STAGE_ROUNDS[s]) return s;
        x -= STAGE_ROUNDS[s];
    }
    return 5;
}

