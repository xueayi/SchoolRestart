function validateAgeRefs(allRowsCollector, idSets) {
    const ids = idSets.allIds;
    for (let r = 1; r <= 101; r++) {
        for (const id of allRowsCollector[r]?.poolIds || []) {
            if (!ids.has(id)) throw new Error(`age round ${r} pool id ${id} missing in events`);
        }
        for (const id of GUARANTEED_BY_ROUND[r] || []) {
            if (!ids.has(id)) throw new Error(`age round ${r} guaranteed id ${id} missing`);
        }
        for (const id of idSets.branchInject?.[r] || []) {
            if (!ids.has(id)) throw new Error(`age round ${r} branch-inject id ${id} missing`);
        }
    }
}

function generateAge(allRowsCollector, idSets) {
    const maxEventCols = 35;
    const maxG = 5;
    const headers = ['$age', ...Array(maxEventCols).fill('event[]'), ...Array(maxG).fill('guaranteed[]')];
    const labels = ['学期', ...Array(maxEventCols).fill('事件池'), ...Array(maxG).fill('保底事件')];

    const rows = [];
    for (let r = 1; r <= 101; r++) {
        const ev = [...(allRowsCollector[r]?.poolIds || [])];
        const inj = idSets.branchInject?.[r];
        if (inj) for (const x of inj) if (!ev.includes(x)) ev.push(x);
        const g = GUARANTEED_BY_ROUND[r] || [];
        const row = [r, ...ev.map(String), ...Array(maxEventCols - ev.length).fill(''), ...g.map(String), ...Array(maxG - g.length).fill('')];
        rows.push(row);
    }
    writeXlsx('age.xlsx', 'age', headers, labels, rows);
}

function validate(idSets) {
    const ids = idSets.allIds;
    for (const row of idSets.rows) {
        const id = Number(row[0]);
        for (let c = 13; c <= 15; c++) {
            const b = row[c];
            if (!b || typeof b !== 'string') continue;
            const m = b.match(/:(\d+)/g);
            if (!m) continue;
            for (const x of m) {
                const tid = Number(x.slice(1));
                if (!ids.has(tid)) throw new Error(`Branch missing event ${tid} from ${id}`);
            }
        }
        if (Number(row[16]) === 1) {
            for (let k = 26; k <= 29; k++) {
                const nx = row[k];
                if (nx !== '' && nx != null && !ids.has(Number(nx))) throw new Error(`choiceNext missing ${nx} from ${id}`);
            }
        }
    }
}

